#include <stdio.h>

int main() 
{
    char s[100]; // ১০০ অক্ষরের একটি জায়গা নিলাম

    // সরাসরি স্পেসসহ পুরো লাইন ইনপুট নেওয়ার জন্য এটাই সবচেয়ে ভালো উপায়
    scanf("%[^\n]%*c", s); 
    
    // প্রথম লাইনে হুবহু এই লেখাটি প্রিন্ট হবে
    printf("Hello, World!\n");
    
    // দ্বিতীয় লাইনে আপনার দেওয়া ইনপুটটি প্রিন্ট হবে
    printf("%s", s);

    return 0;
}
